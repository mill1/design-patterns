package intelliware;

public class CeilingLight {
    
    public void On() {
       System.out.println("Ceiling Light is on.");
    }
    
    public void Off() {
       System.out.println("Ceiling Light is off.");
    }
    
    public void DimDown() {
       System.out.println("Ceiling Light dimmed down");
    }
    
    public void DimUp() {
       System.out.println("Ceiling Light dimmed up");
    }
}
